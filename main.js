//OPERADORES DE ASIGNACIÓN

//1. OPERADOR DE ASIGNACIÓN
console.log("1. OPERADOR DE ASIGNACIÓN");
let a = 12;
let b = 3;
let c = a + b;
console.log("a = ", a);
console.log("b = ", b);
//2. OPERADOR DE SUMA Y SIGNACIÓN
console.log("2. OPERADOR DE SUMA Y ASIGNACIÓN");
console.log("a = ", a);
console.log("b = ", b);
console.log("a += b : a = a + b : ", a+=b);
//3. OPERADOR DE  RESTA Y ASIGNACIÓN
console.log("3. OPERADOR DE RESTA Y ASIGNACIÓN");
let x= 9;
let y = 6;
console.log("x = ", x);
console.log("y = ", y);
console.log("x -= y : x = x - y : ", x-=y);
//4. OPERADOR DE  MULTIPLICACIÓN Y ASIGNACIÓN
console.log("4. OPERADOR DE MULTIPLICACIÓN Y ASIGNACIÓN");
let e= 12;
let d = 4;
console.log("e = ", e);
console.log("d = ", d);
console.log("e *= d : e = e * d : ", e*=d);
//5. OPERADOR DE DIVISIÓN Y ASIGNACIÓN
console.log("5. OPERADOR DE DIVISIÓN Y ASIGNACIÓN");
let f= 34;
let g = 7;
console.log("f = ", f);
console.log("g = ", g);
console.log("f /= g : f = f / g : ", f/=g);
//6. OPERADOR DE MODULO Y ASIGNACIÓN
console.log("6. OPERADOR DE MODULO Y ASIGNACIÓN");
let h = 50;
let i= 10;
console.log("h = ", h);
console.log("i = ", i);
console.log("h %= i : h = h % i : ", h%=i);
//7. OPERADOR DE EXPONENCIACIÓN Y ASIGNACIÓN
console.log("7. OPERADOR DE EXPONENCIACIÓN Y ASIGNACIÓN");
let j= 3;
let l= 10;
console.log("j = ", j);
console.log("l = ", l);
console.log("j **= l : j = j ** l : ", j**=l);