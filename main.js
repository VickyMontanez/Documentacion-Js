//METODOS MATH
function calculaCircunferencia(radio) {
    return 2 * Math.PI * radio;
}
console.log("Math.PI se utilizó para calcular la circunferencia de un circulo de radio (3):");
console.log("Formula: (2 * Math.PI * radio) = ", calculaCircunferencia(3));
console.log("Math.PI se utilizó para calcular la circunferencia de un circulo de radio (12):");
console.log("Formula: (2 * Math.PI * radio) = ", calculaCircunferencia(12));